class S32K312(CoreSightTarget):

    VENDOR = "NXP"

    MEMORY_MAP = MemoryMap(
        FlashRegion(name="pflash",  start=0x00400000, end=0x5fffff, blocksize=0x2000, is_boot_memory=True, algo=FLASH_ALGO_CODE),
        FlashRegion(name="dflash",  start=0x10000000, end=0x1001ffff, blocksize=0x2000, algo=FLASH_ALGO_DATA),
        RamRegion(name="itcm",      start=0x00000000, length=0x10000), # 64 KB
        RamRegion(name="dtcm",      start=0x20000000, length=0x10000), # 64 KB
        RamRegion(name="sram",      start=0x20400000, length=0x18000), # 96 KB
        )

    def __init__(self, session):
        super(S32K312, self).__init__(session, self.MEMORY_MAP)

    def reset(self, reset_type=None):
        super(S32K312, self).reset(self.ResetType.SW_VECTRESET)

    def reset_and_halt(self, reset_type=None, map_to_user=True):
        super(S32K312, self).reset_and_halt(self.ResetType.SW_VECTRESET)

    def create_init_sequence(self):
        seq = super(S32K312, self).create_init_sequence()

        seq.wrap_task('discovery',
            lambda seq: seq
                # Normally the discovery sequence will scan for APs and then add those found
                # to a list. Unfortunately, the S32K312 freaks out when you scan for nonexistent
                # APs, so the list of APs are provided statically here.
                .replace_task('find_aps', self.create_s32k312_aps)

                # Debug needs to be enabled in the SDA_AP before pyOCD can probe for
                # components.
                .insert_before('find_components',
                    ('enable_debug', self.enable_s32k312_debug))
        )

        return seq

    def create_s32k312_aps(self):
        self.dp.valid_aps = AP_ID_LIST

    def enable_s32k312_debug(self):
        self.dp.aps[SDA_AP_ID].write_reg(SDA_AP_DBGENCTRL_ADDR, SDA_AP_EN_ALL)

